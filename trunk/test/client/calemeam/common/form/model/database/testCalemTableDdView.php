        function testCalemTableDdView() {
        	   debug("<b>testCalemTableDdView</b>");
            var ctxt=CalemContext.getInstance();
            var reg=ctxt.getRegistry();
            var wtPmDd=reg.getTableDd('wo_wt_new_pm');
            //Check table name first
            debug("tableName="+wtPmDd.getTableName());
            assertTrue(wtPmDd.getTableName()=='pm');
            
            //Check for cache type
            assertTrue(wtPmDd.isMemoryCached()==false);
            
            //Check for query built out of it
            var tblQry=wtPmDd.buildGetAllQuery();
            debug("tblQry="+tblQry.getSql());
            assertTrue(tblQry.getSql().indexOf('pm')>0);		   
        }
        
        function startTest() {
        		setUp();
        		testCalemTableDdView();
        }
        
        if (location.search.indexOf('localrun')==-1) {
